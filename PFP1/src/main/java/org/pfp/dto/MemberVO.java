package org.pfp.dto;

public class MemberVO {
	private String userId;
	private String userPw;
	private String joinDate;
	private String nickname;
	private String userTel;
	private String email;
	private String address;
	private String postNum;
	private String birthday;
	private int emailReceive;
	private String messageReceive;
	private String note;
	private int auth;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPw() {
		return userPw;
	}
	public void setUserPw(String userPw) {
		this.userPw = userPw;
	}
	public String getJoinDate() {
		return joinDate;
	}
	public void setJoinDate(String joinDate) {
		this.joinDate = joinDate;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getUserTel() {
		return userTel;
	}
	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPostNum() {
		return postNum;
	}
	public void setPostNum(String postNum) {
		this.postNum = postNum;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public int getEmailReceive() {
		return emailReceive;
	}
	public void setEmailReceive(int emailReceive) {
		this.emailReceive = emailReceive;
	}
	public String getMessageReceive() {
		return messageReceive;
	}
	public void setMessageReceive(String messageReceive) {
		this.messageReceive = messageReceive;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public int getAuth() {
		return auth;
	}
	public void setAuth(int auth) {
		this.auth = auth;
	}
	

}
